package modele;

public class SiteTourisrique {
	private String idsitetour, nomsitetour, address, heuredep, heurefin, idvoy;
	private double prix;
	private int idville;
	public SiteTourisrique() {
		super();
	}
	public String getIdsitetour() {
		return idsitetour;
	}
	public void setIdsitetour(String idsitetour) {
		this.idsitetour = idsitetour;
	}
	public String getNomsitetour() {
		return nomsitetour;
	}
	public void setNomsitetour(String nomsitetour) {
		this.nomsitetour = nomsitetour;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getHeuredep() {
		return heuredep;
	}
	public void setHeuredep(String heuredep) {
		this.heuredep = heuredep;
	}
	public String getHeurefin() {
		return heurefin;
	}
	public void setHeurefin(String heurefin) {
		this.heurefin = heurefin;
	}
	public String getIdvoy() {
		return idvoy;
	}
	public void setIdvoy(String idvoy) {
		this.idvoy = idvoy;
	}
	public double getPrix() {
		return prix;
	}
	public void setPrix(double prix) {
		this.prix = prix;
	}
	public int getIdville() {
		return idville;
	}
	public void setIdville(int idville) {
		this.idville = idville;
	}
	
	
}
